package Entity;

/**
 *
 * @author ADMIN
 */
public class Category {
    private String name, note;

    public Category(String name, String note) {
        this.name = name;
        this.note = note;
    }

    public Category() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
    
    
}
